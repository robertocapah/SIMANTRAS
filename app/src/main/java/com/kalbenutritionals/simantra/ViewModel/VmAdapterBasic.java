package com.kalbenutritionals.simantra.ViewModel;

public class VmAdapterBasic {
    private String txtLinkImage;
    private String title;
    private String subtitle;

    public String getTxtLinkImage() {
        return txtLinkImage;
    }

    public void setTxtLinkImage(String txtLinkImage) {
        this.txtLinkImage = txtLinkImage;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public void setSubtitle(String subtitle) {
        this.subtitle = subtitle;
    }
}
